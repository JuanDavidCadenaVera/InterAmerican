    public Eliminar(String email, String contraseña, String tipoUsuario) {
        if(tipoUsuario.equals("estudiante")){
            this.estudiante = new EstudianteA(email,contraseña);
            
        } else if (tipoUsuario.equals("profesor")){
            this.profesor = new ProfesorA(email,contraseña);
        }
        
        initComponents();
        setLocationRelativeTo(null);
        this.administrador = new Administrador (email, contraseña);
        this.nombresEstudiantes = administrador.obtenerNombresEstudiantes();
        this.nombresProfesores = administrador.obtenerNombresProfesores();
        cargarNombres();
        
           if (estudiante != null) {
        LabelNombres1.setFont(new java.awt.Font("Segoe UI", 1, 24));
        LabelNombres1.setText("ESTUDIANTES");
        } else if (profesor != null) {
        LabelNombres1.setFont(new java.awt.Font("Segoe UI", 1, 24));
        LabelNombres1.setText("PROFESORES");
        }
    
    }
    
    private void cargarNombres(){
         DefaultComboBoxModel<String> nombresModel;
    
        if (estudiante != null) {
            String[] nombres = nombresEstudiantes.keySet().toArray(new String[0]);
            Arrays.sort(nombres);
            nombresModel = new DefaultComboBoxModel<>(nombres);
        } else if (profesor != null) {
            String[] nombres = nombresProfesores.keySet().toArray(new String[0]);
            Arrays.sort(nombres);
            nombresModel = new DefaultComboBoxModel<>(nombres);
        } else {
            nombresModel = new DefaultComboBoxModel<>();
    }
        Nombres.setModel(nombresModel);
    }
    
    private String obtenerIdEstudiantes() {
        String nombreSeleccionado = (String) Nombres.getSelectedItem();
        return nombresEstudiantes.get(nombreSeleccionado);
    }
     
    private String obtenerIdProfesor() {
      String nombreSeleccionado = (String) Nombres.getSelectedItem();
      return nombresProfesores.get(nombreSeleccionado);
    }

