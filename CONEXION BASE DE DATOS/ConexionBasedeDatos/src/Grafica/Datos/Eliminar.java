    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
         int opcion = JOptionPane.showConfirmDialog(this, "¿Estás seguro que deseas eliminar?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            if (estudiante != null) {
                administrador.eliminar(obtenerIdEstudiantes());
                nombresEstudiantes.remove((String)Nombres.getSelectedItem());
        } else if (profesor != null) {
                administrador.eliminar(obtenerIdProfesor());
                nombresProfesores.remove((String)Nombres.getSelectedItem());
            }
            cargarNombres();
        }
    }//GEN-LAST:event_EliminarActionPerformed

